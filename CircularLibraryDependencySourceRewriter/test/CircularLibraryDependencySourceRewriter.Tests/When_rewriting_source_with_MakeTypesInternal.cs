using Compze.Utilities.Testing.Must;
using Compze.Utilities.Testing.XUnit.BDD;
using static CircularLibraryDependencySourceRewriter.SourceRewriter;

// ReSharper disable InconsistentNaming

namespace CircularLibraryDependencySourceRewriter.Tests;

public class When_rewriting_source_with_MakeTypesInternal
{
    public class given_a_public_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public class Foo {}");

        [XF]
        public void the_class_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal class Foo {{}}");
    }

    public class given_a_public_interface : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public interface IFoo {}");

        [XF]
        public void the_interface_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal interface IFoo {{}}");
    }

    public class given_a_public_struct : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public struct Bar {}");

        [XF]
        public void the_struct_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal struct Bar {{}}");
    }

    public class given_a_public_enum : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public enum Color { Red, Green }");

        [XF]
        public void the_enum_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal enum Color {{ Red, Green }}");
    }

    public class given_a_public_record : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public record Person(string Name);");

        [XF]
        public void the_record_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal record Person(string Name);");
    }

    public class given_a_public_delegate : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public delegate void MyHandler(int value);");

        [XF]
        public void the_delegate_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal delegate void MyHandler(int value);");
    }

    public class given_a_public_static_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public static class Extensions {}");

        [XF]
        public void the_class_becomes_internal_static() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal static class Extensions {{}}");
    }

    public class given_a_public_abstract_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public abstract class Base {}");

        [XF]
        public void the_class_becomes_internal_abstract() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal abstract class Base {{}}");
    }

    public class given_a_public_sealed_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public sealed class Final {}");

        [XF]
        public void the_class_becomes_internal_sealed() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal sealed class Final {{}}");
    }

    public class given_a_public_partial_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public partial class Widget {}");

        [XF]
        public void the_class_becomes_internal_partial() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal partial class Widget {{}}");
    }

    public class given_public_members_without_type_keywords : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal(
            """
            public class Foo
            {
               public string Name { get; set; }
               public void DoStuff() {}
               public int Count;
               public event EventHandler Changed;
            }
            """);

        [XF]
        public void the_class_becomes_internal_but_members_remain_public() =>
            _result.Must().Be($"{AutoGeneratedHeader}{"""
                                                      internal class Foo
                                                      {
                                                         public string Name { get; set; }
                                                         public void DoStuff() {}
                                                         public int Count;
                                                         public event EventHandler Changed;
                                                      }
                                                      """}");
    }

    public class given_nested_public_classes : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal(
            """
            public class Outer
            {
               public class Inner
               {
                  public class DeepInner {}
               }
            }
            """);

        [XF]
        public void all_type_declarations_become_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}{"""
                                                      internal class Outer
                                                      {
                                                         internal class Inner
                                                         {
                                                            internal class DeepInner {}
                                                         }
                                                      }
                                                      """}");
    }

    public class given_multiple_types_in_one_file : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal(
            """
            public class Foo {}
            public interface IBar {}
            public enum Baz { A, B }
            """);

        [XF]
        public void all_types_become_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}{"""
                                                      internal class Foo {}
                                                      internal interface IBar {}
                                                      internal enum Baz { A, B }
                                                      """}");
    }

    public class given_a_file_with_usings_and_namespace : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal(
            """
            using System;
            using System.Collections.Generic;

            namespace MyLib;

            public class Service
            {
               public void Run() {}
            }
            """);

        [XF]
        public void the_structure_is_preserved_and_only_the_type_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}{"""
                                                      using System;
                                                      using System.Collections.Generic;

                                                      namespace MyLib;

                                                      internal class Service
                                                      {
                                                         public void Run() {}
                                                      }
                                                      """}");
    }

    public class given_an_internal_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("internal class AlreadyInternal {}");

        [XF]
        public void the_class_remains_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal class AlreadyInternal {{}}");
    }

    public class given_a_protected_internal_class : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal(
            """
            public class Outer
            {
               protected internal class Nested {}
            }
            """);

        [XF]
        public void the_outer_becomes_internal_but_protected_internal_is_not_matched() =>
            _result.Must().Be($"{AutoGeneratedHeader}{"""
                                                      internal class Outer
                                                      {
                                                         protected internal class Nested {}
                                                      }
                                                      """}");
    }

    public class given_a_public_record_struct : When_rewriting_source_with_MakeTypesInternal
    {
        readonly string _result = MakeTypesInternal("public record struct Point(int X, int Y);");

        [XF]
        public void the_record_struct_becomes_internal() =>
            _result.Must().Be($"{AutoGeneratedHeader}internal record struct Point(int X, int Y);");
    }
}